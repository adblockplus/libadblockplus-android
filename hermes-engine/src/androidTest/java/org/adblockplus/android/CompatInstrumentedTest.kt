package org.adblockplus.android

import androidx.test.ext.junit.runners.AndroidJUnit4
import androidx.test.platform.app.InstrumentationRegistry
import org.junit.Before
import org.junit.Test
import org.junit.runner.RunWith

/**
 * Instrumented test, which will execute on an Android device.
 *
 * @see [Testing documentation](http://d.android.com/tools/testing)
 */
@RunWith(AndroidJUnit4::class)
class CompatInstrumentedTest {
    private lateinit var engine: AdblockEngine

    @Before
    fun setUp() {
        engine = AdblockEngine(InstrumentationRegistry.getInstrumentation().targetContext)
    }

    @Test
    fun testGet() {
        val result = engine.evaluateJS("fetch(\"google.com\", initObj = {method: \"GET\"})")

        assert(result == "")
    }
}
